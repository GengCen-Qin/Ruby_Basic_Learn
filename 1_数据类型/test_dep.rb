# 变量交换
a = 1
b = 2
# 这里交换 a,b的值
b, a = a, b
p a, b

# 赋值操作
p '-' * 30
x = [1, 2, 3]
# 这里 a获取x第一个值，b获取x第二个值
a, b = x
p a, b

p '-' * 30
x = [1, 2, 3]
# *b把剩余值全部接收
a, *b = x
p a,b

p '-' * 30
# 整数 / 整数 = 整数
# 整数 / 浮点数 = 浮点数
p 1/10 # => 0
p 1/10.0 # => 0.1

p '-' * 30
p 1 + 1.0

